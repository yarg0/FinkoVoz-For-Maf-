local function isInFOV(screenX, screenY, margin) -- ĐĹÍÄĹĐ ÁČÇÍĹŃÎÂ ĎÎ FOV
    margin = margin or 0.25 -- 0.25 = 25% ýęđŕíŕ îň öĺíňđŕ

    local sw, sh = getScreenResolution()

    local cx = sw / 2
    local cy = sh / 2

    local dx = math.abs(screenX - cx) / sw
    local dy = math.abs(screenY - cy) / sh

    return dx < margin and dy < margin
end

local function isIgnored(id)
    id = tonumber(id)
    for _, v in ipairs(settings.ignoreBizIds or {}) do
        if tonumber(v) == id then
            return true
        end
    end
    return false
end

function drawFinkaOnScreen()
    if not Access.allowed then return end
    if not settings.render then return end

    local px, py, pz = getCharCoordinates(PLAYER_PED)
    if not px then return end

    local minMoney = settings.min_money or 0
    local maxDist = settings.max_dist_render or 200
    local maxDistSq = maxDist * maxDist

    for _, biz in ipairs(settings.Finka) do

        if isIgnored(biz.idbiz) then
            goto continue
        end

        local money = (tonumber(biz.finkaKK) or 0) * 1000000 +
                      (tonumber(biz.finkaK) or 0) * 1000

        if money >= minMoney then

            local coord = coordById[tostring(biz.idbiz)]
            if coord then

                local dx = coord.x - px
                local dy = coord.y - py
                local dz = coord.z - pz

                local distSq = dx*dx + dy*dy + dz*dz

                if distSq <= maxDistSq then

                    local ok, sx, sy = convert3DCoordsToScreenEx(coord.x, coord.y, coord.z, true, true)

                    if ok then

                        if not isInFOV(sx, sy, 0.45) then
                            goto continue
                        end

                        local dist = math.sqrt(distSq)

                        local color = 0xFFFF0000
                        if money > 800000 then
                            color = 0xFF00FF00
                        elseif money > 200000 then
                            color = 0xFFFFFF00
                        end

                        if settings.render_circle and distSq < 40000 then
                            Draw3DCircle(coord.x, coord.y, coord.z - 2, 12, color, 2.5, 30)
                        end

                        renderFontDrawTextAlign(
                            font,
                            ('[{ff0000}%s{ffffff}] %s'):format(biz.idbiz, biz.name_biz),
                            sx, sy,
                            0xFFFFFFFF,
                            2
                        )

                        renderFontDrawTextAlign(
                            font,
                            string.format('%.0f ě | {20E10E}%s', dist, formatNumberWithDots(money)),
                            sx, sy + 20,
                            0xFFFFFFFF,
                            2
                        )
                    end
                end
            end
        end

        ::continue::
    end
end

