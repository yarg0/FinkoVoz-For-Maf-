local Access = { allowed = false }
function checkAccess(url)
    local dlstatus = require('moonloader').download_status
    local path = getWorkingDirectory() .. "\\config\\access.json"

    -- ?? óäŕë˙ĺě ńňŕđűé ôŕéë
    if doesFileExist(path) then
        os.remove(path)
    end

    -- ?? ńęŕ÷čâŕĺě íîâűé
    downloadUrlToFile(url, path, function(id, status)
        if status == dlstatus.STATUSEX_ENDDOWNLOAD then

            if not doesFileExist(path) then
                sampAddChatMessage(tag.." Îřčáęŕ çŕăđóçęč access!", -1)
                return
            end

            local f = io.open(path, "r")
            if not f then return end

            local content = f:read("*a")
            f:close()

            -- ôčęń ęîäčđîâęč
            content = encoding.UTF8:decode(content)

            local data = decodeJson(content)

            if not data or not data.users then
                sampAddChatMessage(tag.." Îřčáęŕ JSON access!", -1)
                return
            end

            local _, myId = sampGetPlayerIdByCharHandle(PLAYER_PED)
            local nickname = sampGetPlayerNickname(myId)

            Access.allowed = false

            for _, name in ipairs(data.users) do
                if name:lower() == nickname:lower() then
                    Access.allowed = true
                    break
                end
            end

            if Access.allowed then
                message("Äîńňóď đŕçđĺř¸í! Ďđč˙ňíîăî ďîëüçîâŕíč˙!")
            else
                warning_message('Ó âŕń íĺň äîńňóďŕ! Îáđŕňčňĺńü ę đŕçđŕáîň÷čęó äë˙ âíĺńĺíč˙ â áŕçó!')
                thisScript():unload()
            end
        end
    end)
end

