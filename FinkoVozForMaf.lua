    local result = ""
    local counter = 0

    -- Ďđîőîäčě ďî ńčěâîëŕě ńďđŕâŕ íŕëĺâî
    for i = #formatted, 1, -1 do
        counter = counter + 1
        result = formatted:sub(i, i) .. result

        -- Äîáŕâë˙ĺě ňî÷ęó ęŕćäűĺ 3 öčôđű (ęđîěĺ ńŕěîé ëĺâîé ăđóďďű)
        if counter == 3 and i > 1 then
            result = "." .. result
            counter = 0
        end
    end

    return result
end

